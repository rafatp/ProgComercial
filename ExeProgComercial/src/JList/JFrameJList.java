    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!jTextField1.getText().equals(""))
        {
            jList1.setModel(lst);
            lst.addElement(jTextField1.getText());
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Informe o nome", "ListBox", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButtonLimpaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLimpaActionPerformed
        // TODO add your handling code here:
        if(lst.getSize()<=0)
        {
           JOptionPane.showMessageDialog(this,"Lista Vazia!!","ListBox", JOptionPane.PLAIN_MESSAGE);
        }
        else
        {
            lst.removeAllElements();
        }
    }//GEN-LAST:event_jButtonLimpaActionPerformed

    private void jButtonRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveActionPerformed
        // TODO add your handling code here:
        if(jList1.isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(this,"Escolha um item para apagÃ¡-lo","ListBox", JOptionPane.PLAIN_MESSAGE);
        }
        else
        {
            lst.removeElement(jList1.getSelectedValue());
        }
    }//GEN-LAST:event_jButtonRemoveActionPerformed

